#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>
/**
 * op_add - Returns the sum of two numbers
 * @a: first number
 * @b: second number
 * Return: The sum of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - 5 functions
 * @a: input one
 * @b: input two
 * Return: 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - 5 functions
 * @a: input one
 * @b: input two
 * Return: 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - 5 functions
 * @a: input one
 * @b: input two
 * Return: 0
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - 5 functions
 * @a: input one
 * @b: input two
 * Return: 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
