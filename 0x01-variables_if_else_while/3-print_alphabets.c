#include <stdio.h>
/**
* main -it prints out output using put
*
* Return: always (0)
*
*/
int main(void)
{
    

        char c;

        char d;

        c = 'a';
        c = 'A';
        while
                (c <= 'z') {
                        putchar(c);
                        c++;
                }
        while
                (d <= 'z') {
                        putchar(d);
                        d++;
                }
        putchar('\n');
        return (0);
}

